        $post_data['cus_add2'] = "";
        $post_data['cus_city'] = "";
        $post_data['cus_state'] = "";
        $post_data['cus_postcode'] = "";
        $post_data['cus_country'] = "Bangladesh";
        $post_data['cus_phone'] = $request->customer_mobile;
        $post_data['cus_fax'] = "";

        // SHIPMENT INFORMATION
        $post_data['ship_name'] = "SHINECINE";
        $post_data['ship_add1'] = "Dhaka";
        $post_data['ship_add2'] = "Dhaka";
        $post_data['ship_city'] = "Dhaka";
        $post_data['ship_state'] = "Dhaka";
        $post_data['ship_postcode'] = "1000";
        $post_data['ship_phone'] = "";
        $post_data['ship_country'] = "Bangladesh";

        $post_data['shipping_method'] = "NO";
        $post_data['product_name'] = "Movie Ticket";
        $post_data['product_category'] = "Entertainment";
        $post_data['product_profile'] = "physical-goods";

        // OPTIONAL PARAMETERS
        $post_data['value_a'] = "ref001";
        $post_data['value_b'] = "ref002";
        $post_data['value_c'] = "ref003";
        $post_data['value_d'] = "ref004";

        // Insert payment information into the payments table
        DB::table('payments')->updateOrInsert([
            'transaction_id' => $post_data['tran_id'],
        ], [
            'user_id' => $booking->user_id,
            'movie_id' => $booking->movie_id,
            'date_showtime_id' => $booking->date_showtime_id,
            'total_price' => $post_data['total_amount'],
            'status' => 'Pending',
        ]);

        // Initiate the payment process with SSLCommerz
        $sslc = new SslCommerzNotification();
        $payment_options = $sslc->makePayment($post_data, 'hosted');

        if (!is_array($payment_options)) {
            return back()->with('error', 'Payment initiation failed!');
        }

       
    }

   
